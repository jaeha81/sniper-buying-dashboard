export function getProductById(id: string): Product | undefined {
  return sampleProducts.find((p) => p.id === id)
}

export function getProductsByCategory(category: string): Product[] {
  if (category === 'all') return sampleProducts
  return sampleProducts.filter((p) => p.category === category)
}

export function getTopProductsByScore(limit = 6): Product[] {
  return [...sampleProducts]
    .filter((p) => p.status === 'active' || p.status === 'candidate')
    .sort((a, b) => b.sniperScore - a.sniperScore)
    .slice(0, limit)
}

export function getActiveProducts(): Product[] {
  return sampleProducts.filter((p) => p.status === 'active')
}
